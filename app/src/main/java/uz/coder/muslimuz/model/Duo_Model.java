package uz.coder.muslimuz.model;

public class Duo_Model {
    private String name;
    private String ozi;

    public Duo_Model(String name, String ozi) {
        this.name = name;
        this.ozi = ozi;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOzi() {
        return ozi;
    }

    public void setOzi(String ozi) {
        this.ozi = ozi;
    }
}
